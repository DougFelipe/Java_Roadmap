
/*
Operadores de Incremento e Decremento

`++` e `--`, respectivamente. São usados para aumentar ou diminuir o valor de uma variável em uma unidade.

Neste exemplo, incrementamos e decrementamos um valor usando os operadores de incremento e decremento.
*/
public class IncrementDecrementOperatorsExercise {
    public static void main(String[] args) {
        int count = 10;

        System.out.println("Initial count: " + count);
        count++;
        System.out.println("After increment: " + count);
        count--;
        System.out.println("After decrement: " + count);
    }
}
