
/*
Operadores Aritméticos

São os operadores básicos para operações matemáticas, como adição (`+`), subtração (`-`), multiplicação (`*`), divisão (`/`) e módulo (`%`).

Neste exemplo, utilizamos operadores aritméticos para realizar algumas operações básicas.
*/
public class ArithmeticOperatorsExercise {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
