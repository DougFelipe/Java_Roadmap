
/*
Operadores de Comparação (Relacionais)

Usados para comparar dois valores, incluem igual a (`==`), diferente de (`!=`), maior que (`>`), menor que (`<`), maior ou igual a (`>=`) e menor ou igual a (`<=`).

Neste exemplo, comparamos dois números usando operadores de comparação.
*/
public class ComparisonOperatorsExercise {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        System.out.println("x == y: " + (x == y));
        System.out.println("x != y: " + (x != y));
        System.out.println("x > y: " + (x > y));
        System.out.println("x < y: " + (x < y));
        System.out.println("x >= y: " + (x >= y));
        System.out.println("x <= y: " + (x <= y));
    }
}
