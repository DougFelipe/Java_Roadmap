
/*
Final e Constantes

A palavra-chave 'final' em Java é usada para indicar que algo não pode ser alterado. Variáveis declaradas 
como 'final' são constantes e não podem ser modificadas após a inicialização.

Neste exemplo, demonstramos o uso da palavra-chave 'final' para criar constantes.
*/
public class FinalAndConstantsExercise {
    public static final double PI = 3.141592653589793;

    public static void main(String[] args) {
        System.out.println("Value of PI: " + PI);
    }
}
