
/*
Literais

Literais são valores constantes usados em Java. Existem literais para tipos primitivos como inteiros, 
pontos flutuantes, caracteres e strings.

Neste exemplo, usamos diferentes literais para inicializar variáveis.
*/
public class LiteralsExercise {
    public static void main(String[] args) {
        int decimalLiteral = 100;
        int octalLiteral = 0144; // Representa 100 em base 8
        int hexaLiteral = 0x64;  // Representa 100 em base 16
        
        System.out.println("Decimal Literal: " + decimalLiteral);
        System.out.println("Octal Literal: " + octalLiteral);
        System.out.println("Hexadecimal Literal: " + hexaLiteral);
    }
}
