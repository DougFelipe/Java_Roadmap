
/*
Uso do `else`

Como usar a cláusula `else` em conjunto com `if` para lidar com situações alternativas.

Neste exemplo, avaliamos uma expressão e exibimos mensagens diferentes com base no resultado.
*/
public class ElseUsageExercise {
    public static void main(String[] args) {
        int number = 3;

        if (number > 5) {
            System.out.println("The number is greater than 5.");
        } else {
            System.out.println("The number is 5 or less.");
        }
    }
}
