
/*
Uso do operador ternário com `if-else`

Uso do operador ternário `? :` como alternativa compacta ao `if-else`.

Neste exemplo, usamos o operador ternário para determinar uma mensagem baseada em uma condição.
*/
public class TernaryOperatorExercise {
    public static void main(String[] args) {
        int number = 7;
        String result = (number > 5) ? "The number is greater than 5." : "The number is 5 or less.";
        
        System.out.println(result);
    }
}
