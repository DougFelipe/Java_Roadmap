
/*
Comparação de números e strings

Comparar números e strings dentro da estrutura `if`.

Neste exemplo, comparamos números e strings usando a estrutura `if`.
*/
public class NumberStringComparisonExercise {
    public static void main(String[] args) {
        int number = 10;
        String text = "Hello";

        if (number == 10) {
            System.out.println("The number is 10.");
        }

        if (text.equals("Hello")) {
            System.out.println("The text is 'Hello'.");
        }
    }
}
