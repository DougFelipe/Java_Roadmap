
/*
Avaliação de expressões complexas

Uso de operadores lógicos (&&, ||, !) para avaliar expressões complexas dentro do `if`.

Neste exemplo, avaliamos uma expressão complexa usando operadores lógicos.
*/
public class ComplexExpressionsExercise {
    public static void main(String[] args) {
        int number = 15;
        
        if (number > 10 && number < 20) {
            System.out.println("The number is between 10 and 20.");
        }
    }
}
