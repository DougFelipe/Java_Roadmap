
/*
Sintaxe Básica do `if`

Entender a sintaxe fundamental do `if` e como avaliar uma expressão booleana simples.

Neste exemplo, avaliamos uma expressão simples e exibimos uma mensagem se a condição for verdadeira.
*/
public class BasicIfSyntaxExercise {
    public static void main(String[] args) {
        int number = 10;

        if (number > 5) {
            System.out.println("The number is greater than 5.");
        }
    }
}
