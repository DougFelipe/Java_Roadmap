
/*
Inicialização de Variáveis

Em Java, quando declaramos uma variável, podemos inicializá-la ao mesmo tempo. A inicialização é o processo 
de atribuir um valor inicial a uma variável na hora da declaração.

Neste exemplo, inicializamos diferentes tipos de variáveis no momento da declaração.
*/
public class InitializationExercise {
    public static void main(String[] args) {
        int age = 25;
        double salary = 60000.5;
        char gender = 'F';
        boolean isEmployed = false;
        
        System.out.println("Age: " + age);
        System.out.println("Salary: " + salary);
        System.out.println("Gender: " + gender);
        System.out.println("Is employed: " + isEmployed);
    }
}
