
/*
Uso do loop `for` com arrays e coleções

Iterando sobre elementos de arrays e coleções usando a estrutura `for`.

Neste exemplo, iteramos sobre um array de números usando o loop `for` tradicional.
*/
public class ForWithArraysAndCollectionsExercise {
    public static void main(String[] args) {
        int[] numbers = {1, 2, 3, 4, 5};
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);
        }
    }
}
