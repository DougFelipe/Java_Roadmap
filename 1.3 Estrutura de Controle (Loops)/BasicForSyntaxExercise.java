
/*
Sintaxe Básica do `for`

A estrutura de repetição `for` é utilizada quando se sabe previamente o número de vezes que um bloco de comandos deve ser repetido.

Neste exemplo, usamos o `for` para imprimir números de 1 a 5.
*/
public class BasicForSyntaxExercise {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
}
