
/*
Sintaxe do `while`

A estrutura de repetição `while` avalia uma condição e executa um bloco de código enquanto essa condição for verdadeira.

Neste exemplo, usamos o `while` para imprimir números de 1 a 5.
*/
public class WhileSyntaxExercise {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 5) {
            System.out.println(i);
            i++;
        }
    }
}
