
/*
Introdução a Strings

Strings são sequências de caracteres e são objetos em Java.

Neste exemplo, declaramos e inicializamos algumas strings e imprimimos seus valores.
*/
public class IntroToStringsExercise {
    public static void main(String[] args) {
        String str1 = "Hello";
        String str2 = "World";
        
        System.out.println(str1);
        System.out.println(str2);
    }
}
