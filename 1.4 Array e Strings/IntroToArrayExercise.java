
/*
Introdução aos Arrays

Arrays são estruturas de dados que armazenam múltiplos valores do mesmo tipo em uma única variável.

Neste exemplo, declaramos e inicializamos um array de inteiros e imprimimos seus valores.
*/
public class IntroToArrayExercise {
    public static void main(String[] args) {
        int[] numbers = {1, 2, 3, 4, 5};
        for (int num : numbers) {
            System.out.println(num);
        }
    }
}
